#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*lastone;

	if (*lst)
	{
		lastone = ft_lstlast(*lst);
		lastone->next = new;
	}
	else
		*lst = new;
}
